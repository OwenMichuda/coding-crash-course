# This is a comment! It will not be executed in the program. Anything that starts with a `#` is a comment in Python.
# Comments are used to explain code and are ignored by the interpreter.

print("Hello, World!")
print("You'll notice that each message is printed on a new line.")

print() # This prints a blank line

print("You can also print multiple lines like this:")
print("Line 1\nLine 2\nLine 3")

print()

# More on variables in a bit!
name_intro = "My name is Owen."

print("You can print variables, like this:")
print(name_intro)

print()

my_name = "Owen"
print("And you can print variables with other text, like this:")
print("My name is " + my_name + "!")

print()

print("You can also use f-strings to format your output:")
print(f"My name is {my_name}?")

print()

